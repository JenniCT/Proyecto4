            <h1>Chiapas: Un poco de su historia</h1>
            <p>El nombre de Chiapas proviene de la palabra Chiapan o Tepechiapan, forma en que se designaba a la antigua 
            poblacion indigena de los chiapanecas, y cuyo significado es " Cerro de la Chia" o "Agua debajo del Cerro". 
            Los conquistadores, al fundar dos ciudades en la region, Chiapa de los Indios y Chiapa de los Españoles, 
            adoptaron para ambas el nombre de Provincia de las Chiapas.</p>
            <p>En territorio chiapaneco, los mayas se establecieron y fundaron grandes ciudades, legado del florecimiento
                de una cultura organizada en clases sociales jerárquicas que gobernaban ciudades independientes dentro de 
                un mismo imperio. A partir del declive de esta gran cultura, las ciudades fueron abandonadas y los violentos 
                indios chiapas se apoderaron del territorio; subyugando a otras civilizaciones prehispánicas de la región 
                como los zoques, mames y tzeltales. En 1521, los conquistadores que controlaban la Villa del Espíritu 
                Santo en Coatzacoalcos, iniciaron la expedición para conquistar tierras chiapanecas.
                <br> <br> El primer intento fue un fracaso gracias a la ferocidad de los guerreros chiapas, pero cuando el español 
                Diego de Mazariegos arribó con un ejército mejor organizado, estableció la Villa Real de Chiapas, hoy San 
                Cristóbal de las Casas, en honor del obispo de Chiapas, Fray Bartolomé de las Casas.
            </p>
            <p>
                Pero hasta en 1523 que Hernán Cortes envia a Pedro de Alvarado, Luis Marin y Diego de Mazariegos en declaracion total de conquista. 
                Se inicia asi un intento de sujecion que jamas ha podido ser concretado cotra los indigenas de Chiapas. <br><br>

                Digna de mencion es la epopeya portentosa de los valientes e indomables chiapanecas. En 1527, acosados por un batallon de españoles, 
                guerreros mexicas y tlaxcaltecas, aliadosa estos, deciden perder la vida antes que la libertad arrojandose al Cañon del Sumidero con 
                sus familias y sus pertenencias. No a la esclavitud, no a la sumision. Luego de la conquista militar y la posterior conquista espiritual, 
                los españoles decidieron espiritual fundar una ciudad en las inmediaciones de Socton-Nandalumi. Como era su costumbre, lavantaron la Casa 
                de Cabildos y el Ayuntamiento; cuadricularon el terreno y fundaron, el 3 de marzo de 1528, Chiapa de los Indios
                (hoy Chiapa de Corzo), primera ciudad del estado. Pero las condiciones ambientales adversas al gusto español pronto los obligarona buscar 
                un nuevo sitio en el hermoso valle de Hueyacatlan, y el 31 de marzo de 1528 fundaron la Villa Real o Chiapa de los Españoles(hoy San 
                Cristobal de las Casas). En 1556 se fundo San Pablo Comitan, y hacia fines del siglo XVI el pequeño cacerio de Tuchtlan se convirtio 
                en el pueblo de San Marcos de Tuxtla (hoy Tuxtla Gutierrez). Por su lejania con la capital del virreinato, Chiapas estuvo casi ajena a 
                las batallas que origino la guerra de la Independencia contra la corona española. Pero una vez consumada esta, los chiapanecos debieron 
                decidir su adhesion al nuevo pais.
            </p>
            <p>
                El 28 de agosto de 1821, Fray Matias de Cordova proclamo la independencia de la Ciudad de Comitan respecto a la corona española y adopto 
                el sistema imperial de Agustin de Iturbide. A ella se adherieron todas las comunidades importantes de la region, incluso, la misma Guatemala. 
                Un año despues, la Regencia del Imperio Mexicano anexo a Chiapas como su Territorio. Tras la caida del Imperio, con la muerte de Iturbide, 
                la adhesion chiapaneca se vio a la deriva y tuvo que ser mediante un plebiscito para que los habitantes de toda la region decidieran su 
                situacion politica. Asi, el 14 se Septiembre de 1824 se hizo la declaracion solemne de la incorporacion de Chiapas a Mexico. Tras este 
                hecho sin precedente, Chiapas demostro ser un cuna de la democracia mas pura, distinguiendose por siempre como el unico Estado Mexicano 
                por decision propia.
            </p>
